export function convertText(text: string, styleId: string): string {
  if (styleId === "upside_down") {
    const map = MAPS[styleId];
    return text
      .split("")
      .reverse()
      .map((char) => {
        const index = ALPHABET.indexOf(char);
        if (index === -1) return char;
        return [...map][index] || char;
      })
      .join("");
  }

  if (styleId === "wide_spaced") {
    return text.split("").join(" ");
  }

  if (styleId === "alternating_caps") {
    let toggle = 0;
    return text
      .split("")
      .map((char) => {
        if (/[a-zA-Z]/.test(char)) {
          const result =
            toggle % 2 === 0 ? char.toUpperCase() : char.toLowerCase();
          toggle += 1;
          return result;
        }
        return char;
      })
      .join("");
  }

  if (styleId === "zalgo") {
    return text
      .split("")
      .map((char) => {
        if (char === " ") return char;
        let result = char;
        const count = Math.floor(Math.random() * 3) + 1;
        for (let i = 0; i < count; i += 1) {
          const mark =
            ZALGO_MARKS[Math.floor(Math.random() * ZALGO_MARKS.length)];
          result += mark;
        }
        return result;
      })
      .join("");
  }

  if (styleId === "bracketed") {
    if (!text) return text;
    return `ã€${text}ã€‘`;
  }

  if (styleId === "strikethrough") {
    return text
      .split("")
      .map((c) => c + "\u0336")
      .join("");
  }

  if (styleId === "underline") {
    return text
      .split("")
      .map((c) => c + "\u0332")
      .join("");
  }

  if (styleId === "slash_through") {
    return text
      .split("")
      .map((c) => c + "\u0338")
      .join("");
  }

  if (styleId === "tilde_strike") {
    return text
      .split("")
      .map((c) => c + "\u0334")
      .join("");
  }

  const map = MAPS[styleId];
  if (!map) return text;

  return text
    .split("")
    .map((char) => {
      const index = ALPHABET.indexOf(char);
      if (index === -1) return char;
      return [...map][index] || char;
    })
    .join("");
}
